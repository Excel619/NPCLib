package com.gmail.excel8392.npclib.grid;

public class GridLocation {

    private final short x;
    private final short y;

    public GridLocation(short x, short y) {
        this.x = x;
        this.y = y;
    }

    public short getX() {
        return x;
    }

    public short getY() {
        return y;
    }

}
